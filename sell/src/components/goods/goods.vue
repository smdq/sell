<template>
  <div class="goods">
    <div class="menu-wrapper" ref="menu">
      <ul>
        <li class="menu-item border-bottom" @click="selectMenu(index,$event)"  :class="{'current':currentIndex ===index}" v-for="(item,index) in goods" :key="index">
          <span class="text">
            <span v-show="item.type>0" class="icon " :class="classMap[item.type]"></span>{{item.name}}
          </span>
        </li>
      </ul>
    </div>
    <div class="foods-wrapper" ref="foods">
      <ul>
        <li class="food.list food-list-hook" v-for="(item,index) in goods" :key="index">
          <h1 class="title">{{item.name}}</h1>
          <ul>
            <li class="food-item border-bottom" v-for="(food,index) in item.foods" :key="index">
              <div class="icon">
                <img width="57" height="57" :src="food.icon" alt="">
              </div>
              <div class="content">
                <h2 class="name">{{food.name}}</h2>
                <p class="desc">{{food.description}}</p>
                <div class="extra">
                  <span>月售{{food.sellCount}}份</span>
                  <span>好评率{{food.rating}}%</span>
                </div>
                <div class="price">
                  <span class="now">￥{{food.price}}</span>
                  <span class="old" v-show="food.oldPrice">￥{{food.oldPrice}}</span>
                </div>
                <div class="cartcontrol-wrapper">
                  <cartcontrol @cartadd="addChange" :food="food"></cartcontrol>
                </div>
              </div>
            </li>
          </ul>
        </li>
      </ul>
    </div>
    <shop-cart ref="shopcart" :selectFoods="selectFoods" :deliveryPrice="seller.deliveryPrice" :minPrice="seller.minPrice"></shop-cart>
  </div>
</template>
<script>
import BScroll from 'better-scroll'
import shopCart from '@/components/shopcart/shopcart'
import cartcontrol from '@/components/cartcontrol/cartcontrol'
const ERR_OK = 0
export default {
  props:{
    seller:{
      type:Object
    }
  },
  data(){
   return {
     goods:[],
     classMap:[],
     listHeight:[],
     scrollY:0
   }
  },
  created(){
    fetch('/api/goods')
    .then(res => res.json())
    .then(response =>{
      if(response.errno == ERR_OK){
        this.goods = response.data;
        console.log(this.goods)

        this.$nextTick( () =>{

          this._initScroll()
          this._calculateHeight()
         
        })
      }
    })

    

    this.classMap = ['decrease','discount','special','invoice','guarantee']
  },
  computed:{
   currentIndex(){
     for(let i =0;i<this.listHeight.length;i++){
       let height1 = this.listHeight[i]
       let height2 = this.listHeight[i+1]
       if(!height2 || (this.scrollY >= height1 && this.scrollY < height2)){
         return i
       }
     }
     return 0
   },
   selectFoods(){
     let foods =[];
     this.goods.forEach((good) =>{
       good.foods.forEach((food)=>{
         if(food.count){
           foods.push(food)
         }
       })
     })
     return foods
   }
   
  },
  methods:{
    selectMenu(index,event){
     
     /* 当pcd端时event._constructed监听到时  不执行，当自己自定义派发是值为true才走下面index */
     if(!event._constructed) {
       return
     }

     let foodList = this.$refs.foods.getElementsByClassName('food-list-hook')
     let el = foodList[index]
     this.foodsScroll.scrollToElement(el)
      //console.log(index)
      
     
    },
    _initScroll(){

      this.menuScroll = new BScroll(this.$refs.menu,{click:true})

      this.foodsScroll = new BScroll(this.$refs.foods,{
        // 实时监听滚动位置参数
        probeType: 3,
        click:true
      })

      this.foodsScroll.on('scroll',(pos) =>{
        this.scrollY = Math.abs(pos.y)
        //console.log(this.scrollY)
      })
    },

    _calculateHeight(){
      let foodList = this.$refs.foods.getElementsByClassName('food-list-hook')

      let height = 0;
      this.listHeight.push(height)
      for(let i=0;i<foodList.length;i++){
        let item = foodList[i]
        height += item.clientHeight
        this.listHeight.push(height)
      }
      //console.log(this.listHeight)
    },
    _drop(target){
      this.$refs.shopcart.drop(target);
    },
    addChange(target){
     this._drop(target)
    }

  },
  components:{
    shopCart,
    cartcontrol
  }
}
</script>
